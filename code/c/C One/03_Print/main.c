#include <stdio.h>
#include <stdlib.h>

int main() {

    // \n - Escape sequence New line
    printf("Hello\n"); // string
    printf("%d\n",12);   // Decimel
    printf("%c\n",'a');  // Character
    printf("%f\n",3.2);  // Floats
    printf("%s\n","Blink"); // String

    return 0;



}