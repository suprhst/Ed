#include <stdio.h>
#include <stdlib.h>

int main() {
    // Triangle

    printf("*       \n");
    printf("**      \n");
    printf("***     \n");
    printf("****    \n");
    printf("*****   \n");
    printf("******  \n");
    printf("******* \n");

    // Another Triangle 
    printf("|\\ \n");
    printf("| \\         \n"); // \\ for escaping escape sequence \ 
    printf("| \\        \n");
    printf("|  \\       \n");
    printf("|   \\      \n");
    printf("|    \\     \n");
    printf("|     \\    \n");
    printf("|______\\   \n");

    


    return 0;
    
}