fn main() {

    //Integer 32 bit

    let a: i32 = 5;
    println!("The value of A {} ",a);
    let x: i32 = 6;

    // Integer 64 bit 

    let b: i64 = 12;
    println!("The value of B {}",b);

    // Unsigned 8 bit int

    let c: u8 = 1;
    println!("The value of C {}",c);

    // Floating point

    let d: f64 = 2.6;
    println!("The value of D {}",d);
    let y: f64 = 5.6;

    let e: f32 = 3.5;
    println!("The value of E {}",e);

    let sum: i32 = a + x;
    println!("Total of A + X = {}",sum);
    
    let difference: f64 = d-y;
    println!("The difference between {} and {} = {}",d,y,difference);

    let times: i32 = a*4;
    println!("{} multiplied by  4 = {}",a,times);


    let k: char = '@';
    println!("Character {}",k);

    



}
