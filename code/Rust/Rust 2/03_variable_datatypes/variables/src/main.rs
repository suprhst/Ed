fn main() {
    
    // Datatypes

    // Signed int

    let a:i8 = 1;
    let b:i16 = 12;
    let c:i32 = 400;
    let d:i64 = 123456;
    let e:i128 = -55555;

    println!("Value of  a = {} ",a);
    println!("Value of  b = {} ",b);
    println!("Value of  c = {} ",c);
    println!("Value of  d = {} ",d);
    println!("Value of  e = {} ",e);

    
    // Unsigned int
    
    let f:u8 = 1;
    let g:u16 = 3;
    let h:u32 = 30;
    let i:u64 = 650;
    let j:u128 = 1456465;

    println!("Value of  f = {} ",f);
    println!("Value of  g = {} ",g);
    println!("Value of  h = {} ",h);
    println!("Value of  i = {} ",i);
    println!("Value of  j = {} ",j);

    // Float

    let k = 6.7;
    let l:f32 =1.9;
    let m:f64 =-0.64;
    
    println!("The valye of k = {}",k);
    println!("The valye of l = {}",l);
    println!("The valye of m = {}",m);

    // Boolean

    let n = true;
    let o:bool = false;
    println!("Value of n = {}",n);
    println!("Value of o = {}",o);

    //character

    let p:char = 'a';
    
    println!("Value of p = {}",p);

}
